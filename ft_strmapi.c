#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char					*result;
	register unsigned int	i;

	if (!s)
		return (0x0);
	result = malloc(ft_strlen(s) + 1);
	if (!result)
		return (0x0);
	i = 0;
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
